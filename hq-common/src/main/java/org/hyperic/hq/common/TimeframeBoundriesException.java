package org.hyperic.hq.common;

public class TimeframeBoundriesException extends TimeframeBaseException {
    public TimeframeBoundriesException() {
        super();
    }

    public TimeframeBoundriesException(String s) {
        super(s);
    }

    public TimeframeBoundriesException(Throwable t) {
        super(t);
    }

    public TimeframeBoundriesException(String s, Throwable t) {
        super(s, t);
    }
}